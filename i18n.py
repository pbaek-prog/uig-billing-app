def t(key, lang="en"):
    """Translate a key to the specified language. Falls back to English."""
    translations = TRANSLATIONS.get(lang, TRANSLATIONS["en"])
    result = translations.get(key)
    if result is None:
        # Fallback to English
        result = TRANSLATIONS["en"].get(key, key)
    return result


